using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Exercicio_Pratico_de_Classes
{
    internal class Retangulo
    {
        double Largura;
        double Altura;

        public double Area()
        {
            return Largura;
        }

        public double Perimetro () {
            return Altura;
        }

        public double Diagonal()
        {
            return Area();
        }
    }


    }
}
